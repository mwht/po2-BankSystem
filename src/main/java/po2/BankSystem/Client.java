package po2.BankSystem;

public class Client {
	private int id;
	private String name;
	private String surname;
	private long pesel;
	private String address;
	private double balance;
	
	public Client() {
		id = 0;
		name = "";
		surname = "";
		pesel = 0;
		address = "";
		balance = 0;
	}
	
	public Client(int initialId) {
		id = initialId;
		name = "";
		surname = "";
		pesel = 0;
		address = "";
		balance = 0;
	}
	
	public int getId() {
		return id;
	}
	 
	public void setId(int newId) {
		id = newId;
	}
	
	public String getName() {
		return name;
	}
	 
	public void setName(String newName) {
		name = newName;
	}
	
	public String getSurname() {
		return surname;
	}
	 
	public void setId(int newId) {
		id = newId;
	}
	
	public int getId() {
		return id;
	}
	 
	public void setId(int newId) {
		id = newId;
	}
	
	public int getId() {
		return id;
	}
	 
	public void setId(int newId) {
		id = newId;
	}
	
	public int getId() {
		return id;
	}
	 
	public void setId(int newId) {
		id = newId;
	}
}
